/**
 * add - add 2 numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: result of addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract 2 numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: result of subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply 2 numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: result of multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide 2 numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: result of division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - remainder of division of 2 numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}

